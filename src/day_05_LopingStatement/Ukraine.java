package day_05_LopingStatement;

public class Ukraine {
   public static void main(String[] args) {
	
	   //print number 1 to 10
	   
	   int i=1;   
	   while(i<=10)
	   {
		   System.out.println(i);
		   i++;
	   }
	   
	   System.out.println("--------");
	   
	   for(int k=1; k<=10; k++) {
		   System.out.println(k);
	   }
}
}
